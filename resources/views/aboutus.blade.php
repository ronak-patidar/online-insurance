<h1 style="text-align: center;">Insurance is complicated,<br>
we’re here to simplify it.</h1>
<p>Let’s talk about bread!<br>
<br>
The way we know it today, sliced form, was not how it was a century ago. It had to be knifed, torn, or bitten-off from the loaf itself. Complicated, right? Especially now that you can easily toast a slice. Pop, so simple!</p>
<p>But why are we talking about bread? Because insurance today is very much like that unsliced loaf of bread; cumbersome and unnecessarily complicated. Most consumers have a lack of understanding of what product is right for them, and claim settlement is a long, tedious process where paperwork and fine print make the entire experience unpleasant. Aargh, someone get us a knife here!</p>
<h2 style="text-align: center;">We’re simplifying insurance products so that even a 15-year-old can comprehend them.</h2>
<p>We believe this can be changed; the way insurance products are viewed, purchased, and claimed. Simply put, we are here ‘To Make Insurance Simple.’<br>
</p>
<p>With that as our mission, we are reimagining products, and redesigning processes. We are going back to the basics and building simple and transparent insurance solutions, that matter to people.</p>
